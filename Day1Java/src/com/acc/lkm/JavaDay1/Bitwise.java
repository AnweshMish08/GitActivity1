package com.acc.lkm.JavaDay1;

public class Bitwise {
	public static void main(String[] args) {
		boolean x = true;
		boolean y = false;
		boolean a = x | y;
		boolean b = x ^ y;
		boolean c = !x;
		boolean d = x & y;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}
